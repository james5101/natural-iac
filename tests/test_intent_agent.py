class TestComponentRoleMapping:
    @pytest.mark.asyncio
    async def test_all_roles_parse(self, agent, mock_client):
        """Ensure every ComponentRole value survives a round-trip through the agent."""
        for role in ComponentRole:
            payload = {
                "contract": {
                    "name": f"test-{role.value.replace('_', '-')}",
                    "components": [{"name": "svc", "role": role.value}],
                }
            }
            mock_client.messages.create.return_value = _api_response(
                _tool_use_block("emit_contract", payload)
            )

            result = await agent.parse(f"I need a {role.value}")

            assert result.contract.components[0].role == role
