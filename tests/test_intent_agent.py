def _tool_use_block(name: str, input_data: dict, block_id: str | None = None):
    block = MagicMock()
    block.type = "tool_use"
    block.name = name
    block.id = block_id or f"tu_{uuid.uuid4().hex[:8]}"
    block.input = input_data
    return block


def _text_block(text: str):
    block = MagicMock()
    block.type = "text"
    block.text = text
    return block


def _api_response(*blocks):
    resp = MagicMock()
    resp.content = list(blocks)
    resp.stop_reason = "tool_use" if any(b.type == "tool_use" for b in blocks) else "end_turn"
    return resp


def _minimal_contract_payload(name: str = "test-app") -> dict:
    return {
        "contract": {
            "schema_version": "v1",
            "name": name,
            "description": "A simple test app",
            "components": [
                {
                    "name": "api",
                    "role": "web_api",
                    "requirements": {
                        "availability": "standard",
                        "size_hint": "small",
                        "publicly_accessible": True,
                    },
                },
                {
                    "name": "db",
                    "role": "primary_datastore",
                    "requirements": {
                        "availability": "standard",
                        "size_hint": "small",
                        "publicly_accessible": False,
                    },
                },
            ],
        }
    }


