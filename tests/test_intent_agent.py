class TestClarificationLoop:
    @pytest.mark.asyncio
    async def test_single_clarification_then_emit(self, agent, mock_client):
        clarification_id = "tu_clarify_01"

        # Turn 1: ask a question
        turn1 = _api_response(
            _tool_use_block("ask_clarification", {"question": "Is this customer-facing?"}, clarification_id)
        )
        # Turn 2: emit contract
        turn2 = _api_response(
            _tool_use_block("emit_contract", _minimal_contract_payload())
        )
        mock_client.messages.create.side_effect = [turn1, turn2]

        answers = ["Yes, it's customer-facing in production."]
        answer_iter = iter(answers)

        async def callback(q: str) -> str:
            return next(answer_iter)

        result = await agent.parse("I need a web API", clarification_callback=callback)

        assert result.contract is not None
        assert result.trace.questions_asked == ["Is this customer-facing?"]
        assert mock_client.messages.create.call_count == 2

    @pytest.mark.asyncio
    async def test_no_callback_skips_clarification_questions(self, agent, mock_client):
        """Without a callback, the agent responds to questions with a no-answer sentinel."""
        clarification_id = "tu_clarify_01"
        turn1 = _api_response(
            _tool_use_block("ask_clarification", {"question": "What availability do you need?"}, clarification_id)
        )
        turn2 = _api_response(
            _tool_use_block("emit_contract", _minimal_contract_payload())
        )
        mock_client.messages.create.side_effect = [turn1, turn2]

        result = await agent.parse("I need a web API", clarification_callback=None)

        assert result.contract is not None
        # Question recorded in trace even without callback
        assert "What availability do you need?" in result.trace.questions_asked

    @pytest.mark.asyncio
    async def test_max_clarifications_forces_emit(self, mock_client):
        """After max_clarifications rounds, a forcing message is injected."""
        agent = IntentAgent(client=mock_client, model="claude-test", max_clarifications=2)

        def make_question(i):
            return _api_response(
                _tool_use_block("ask_clarification", {"question": f"Question {i}?"}, f"tu_q{i}")
            )

        emit_response = _api_response(
            _tool_use_block("emit_contract", _minimal_contract_payload())
        )

        # Two questions, then an emit
        mock_client.messages.create.side_effect = [
            make_question(1),
            make_question(2),
            emit_response,
        ]

        answers = iter(["answer 1", "answer 2"])

        async def callback(q: str) -> str:
            return next(answers)

        result = await agent.parse("vague intent", clarification_callback=callback)

        assert result.contract is not None
        assert len(result.trace.questions_asked) == 2
        # The forcing message should appear in the trace
        forcing_turns = [
            t for t in result.trace.turns
            if isinstance(t.content, str) and "forcing emit" in t.content
        ]
        assert len(forcing_turns) == 1


